let text = "Olá," + " " + "mundo!";
console.log(text); // Resultado será "Olá, mundo!"

let incrementador = 5;
console.log("Valor inicial:", incrementador); // 5

incrementador = incrementador + 2;
console.log("Após incremento de 2:", incrementador); // 7

incrementador += 3;
console.log("Após incremento de 3:", incrementador); // 10

incrementador++; // Incrementa em 1
console.log("Após incremento de 1:", incrementador); // 11

let igualdade1 = 3;
let igualdade2 = "3";

console.log("Igualdade de 3 == '3':", igualdade1 == igualdade2); // true (compara valor)
console.log("Igualdade de 3 === '3':", igualdade1 === igualdade2); // false (compara valor e tipo)
